def get_temp_regime(temp: float) -> str:
    """Определяет погодный режим по температуре."""
    if temp < -10:
        return "сильный_мороз"
    elif temp < 0:
        return "мороз"
    elif temp < 8:
        return "холодно"
    elif temp < 15:
        return "прохладно"
    elif temp < 22:
        return "тепло"
    else:
        return "жара"


def get_placeholder_label(slot: str, colortype: str, regime: str) -> str | None:
    """
    Возвращает подпись плейсхолдера для текста брифа (именительный падеж).
    None если слот не нужен при данной погоде.
    """
    item_type = SEASON_SLOT_TYPES.get(slot, {}).get(regime)
    if item_type is None:
        return None  # слот не нужен

    palette = COLORTYPE_PALETTES.get(colortype, COLORTYPE_PALETTES["default"])
    colors = palette.get(slot, ["нейтральная"])
    color_rec = colors[0]

    nom = NOMINATIVE_LABELS.get(slot, slot)
    return f"{nom} — {color_rec} (нет в гардеробе)"


